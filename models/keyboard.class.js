/**
 * Represents the keyboard input state for the game. This class holds boolean flags
 * for various keys to track whether they are currently pressed down.
 */
class Kayboard {
    /**
     * Tracks the state of the LEFT arrow key. True if pressed, otherwise false.
     * @type {boolean}
     */
    LEFT = false;


    /**
     * Tracks the state of the RIGHT arrow key. True if pressed, otherwise false.
     * @type {boolean}
     */
    RIGHT = false;


    /**
     * Tracks the state of the UP arrow key. True if pressed, otherwise false.
     * @type {boolean}
     */
    UP = false;


    /**
     * Tracks the state of the DOWN arrow key. True if pressed, otherwise false.
     * @type {boolean}
     */
    DOWN = false;


    /**
     * Tracks the state of the SPACE bar. True if pressed, otherwise false.
     * @type {boolean}
     */
    SPACE = false;


    /**
     * Tracks the state of the 'D' key. True if pressed, otherwise false.
     * @type {boolean}
     */
    D = false;
}